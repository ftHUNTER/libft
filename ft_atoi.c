#include "libft.h"

int	ft_atoi(const char	*str)
{
	size_t	i;
	size_t	y;
	int		x;

	i = 0;
	y = 0;
	x = 1;
	while ((str[i] >= 9 && str[i] <= 13) || (str[i] == 32))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			x = x * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		y = y * 10 + ((str[i] - 48) % 10);
		if (y > 9223372036854775807 && x == -1)
			return (0);
		if (y > 9223372036854775807 && x == 1)
			return (-1);
		i++;
	}
	return (y * x);
}
